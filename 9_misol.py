# A ta bino bor har bitta binoda B tadan qavat bor. Har qavatda C ta xona bor.
#Har bitta xonada D ta parta bor. Umumiy nechta parta bor.
A = int(input("Bino sonini kiriting: "))
B = int(input("Qavat sonini kiriting: "))
C = int(input("Xona sonini kiriting: "))
D = int(input("Parta sonini kiriting: "))

result = A * B * C * D
print(f"{A} ta bino bor. Har bitta binoda {B} tadan qavat bor. Har qavatda {C} xona bor. Har bitta xonada {D} ta parta bor. Umumiy {result} ta parta bor")
